function hitNextFahrschulDirection(player)
	if(player==getLocalPlayer())then
		lastMarkerID=lastMarkerID+1
		destroyElement(source)
		destroyElement(fahrblip)
		if not(lastMarkerID>table.getn(fahrschulMainMarker))then
			marker=createMarker( fahrschulMainMarker[lastMarkerID][1],fahrschulMainMarker[lastMarkerID][2],fahrschulMainMarker[lastMarkerID][3], "checkpoint", 3.0, 255, 0, 0, 150 )
			fahrblip=createBlip(fahrschulMainMarker[lastMarkerID][1],fahrschulMainMarker[lastMarkerID][2],fahrschulMainMarker[lastMarkerID][3],0)
			setElementDimension(fahrblip,dim)
			if(lastMarkerID==table.getn(fahrschulMainMarker))then
				setMarkerTarget ( marker,fahrschulMainMarker[lastMarkerID][1],fahrschulMainMarker[lastMarkerID][2],fahrschulMainMarker[lastMarkerID][3] )
			else
				setMarkerTarget ( marker,fahrschulMainMarker[lastMarkerID+1][1],fahrschulMainMarker[lastMarkerID+1][2],fahrschulMainMarker[lastMarkerID+1][3] )
			end
			setElementDimension(marker,dim)
			addEventHandler("onClientMarkerHit",marker,hitNextFahrschulDirection,false)
		else
			if(table.getn(fahrschulCheckMarker)>hitMarkers)then
				outputChatBox("Du bist leider durchgefallen!",255,0,0)
				for theKey,theTable in ipairs(fahrschulmarkerTable)do
					if(isElement(theTable))then
						destroyElement(theTable)
					end
				end
				triggerServerEvent("canHaveFuerherLic",getLocalPlayer(),false)
				setElementDimension(getLocalPlayer(),0)
				setElementInterior(getLocalPlayer(),3,362.9921875,173.8134765625,1008)
				setElementPosition(getLocalPlayer(),362.9921875,173.8134765625,1008)
				
			else
				triggerServerEvent("canHaveFuerherLic",getLocalPlayer(),true)
				setElementDimension(getLocalPlayer(),0)
				setElementInterior(getLocalPlayer(),3,362.9921875,173.8134765625,1008)
				setElementPosition(getLocalPlayer(),362.9921875,173.8134765625,1008)				
				outputChatBox("Glückwunsch, du hast bestanden!",0,255,0)
			end
		end
	
	end
end

function HitFahrchulCheckMarker(player)
	if(player==getLocalPlayer())then
		local veh=getPedOccupiedVehicle(getLocalPlayer())
		local vx,vy,vz=getElementVelocity(veh)
		if(math.sqrt(vx*vx+vy*vy+vz*vz)<0.48)then
			hitMarkers=hitMarkers+1			
			--outputChatBox(hitMarkers.." / "..table.getn(fahrschulCheckMarker))	
		end
		destroyElement(source)
	end
end











